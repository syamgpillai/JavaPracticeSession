package CollectionsListConcept;

public class Employee {
	
	//this is class is for demonstrating the storing of class objects in arraylist.
	
	
	String name;
	int age;
	String dept;
	
	//now lets create a constructor
	
	Employee(String name, int age, String dept){ //this is the constructor
		
		this.name = name;
		this.age = age;
		this.dept = dept; //this is how we initialize the constructor.
	}

}
